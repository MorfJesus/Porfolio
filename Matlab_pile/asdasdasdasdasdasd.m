u_1=0.01; %м, предельное упругое перемещение
P_1=1; %Н, усилие в амортизаторе, соотв. u_1
u_2=0.05; %м, предельное перемещение
P_2=2; %Н, предельное усилие в амортизаторе, соотв.u_2
k_1=P_1/u_1; %Н/м, жесткость участка упругого деформирования
k_2=(P_2-P_1)/(u_2-u_1);
u=0 : 0.001: u_2;

for u=0 : 0.001: u_2
if u <= u_1
P=k_1*u;
else
P=k_2*(u-sign(u)*u_1)+sign(u)*P_1;
end
end
plot(u, P)